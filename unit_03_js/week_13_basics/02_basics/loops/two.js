// while loop ++++++++++++++++++++++++++++

let index = 1
while (index <= 12) {
    // console.log(`value of index is ${index}`)
    index = index + 3
}


// array in while loop

let myArr = [1, 3, 4, 5, 8, 2]

let arr = 0
while (arr < myArr.length) {
    // console.log(`value is ${myarr[arr]}`)
    arr = arr + 1
}

// do while loop +++++++++++++++++++++


let score = 6

do {
    console.log(`Score is ${score}`);
    score++
} while (score <= 10);